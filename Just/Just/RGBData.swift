import Foundation

class RGBData {
    var r: UInt8
    var g: UInt8
    var b: UInt8
    var a: UInt8
    init(r: UInt8, g: UInt8, b: UInt8, a: UInt8) {
        self.r = r
        self.g = g
        self.b = b
        self.a = a
    }
}
